package moriUse;

import java.util.Random;

/*
 * Random类用来随机生成数字
 * 1.导包 
 * import java.util.Random;
 * 
 * 2.创建
 * Random r = new Random();小括号后面留空即可
 * 
 * 3.使用
  *  获取一个随机的int数字(范围是int所有范围，有正负两种) int num = r.nextInt(); 
  *  获取一个随机的int数字(参数代表了范围，左闭右开区间) int num = r.nextInt(3); 
 * 实际上代表的含义是[0,3),也就是0到2.
 * */

